def write_topic_report(topics: list[dict],
                       report_dir: Path,
                       run_mode: str,
                       logger: logging.Logger):
    """
    Write a human-readable topic discovery report to logs/topic_reports/.
    One file per run, timestamped. Primary tool for exploring discovered topics
    without writing SQL.
    """
    report_dir.mkdir(parents=True, exist_ok=True)
    run_ts      = datetime.now().strftime("%Y-%m-%d_%H-%M")
    report_path = report_dir / f"topic_report_{run_ts}.txt"

    lines = [
        "=" * 70,
        f"  TOPIC DISCOVERY REPORT",
        f"  Run mode:  {run_mode.upper()}",
        f"  Generated: {datetime.now().strftime('%Y-%m-%d %H:%M')}",
        f"  Topics:    {len(topics)}",
        "=" * 70,
        "",
    ]

    # Sort by video count descending
    for t in sorted(topics, key=lambda x: x["video_count"], reverse=True):
        lines += [
            f"Topic {t['topic_id']:>4}  |  {t['topic_label']}",
            f"  Category:  {t['category_name'] or 'Unassigned'}",
            f"  Videos:    {t['video_count']:,}",
            f"  Keywords:  {', '.join(t['keywords'][:10])}",
            "",
        ]

    report_path.write_text("\n".join(lines), encoding="utf-8")
    logger.info(f"Topic report saved   → {report_path}")


