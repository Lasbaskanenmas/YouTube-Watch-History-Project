# Core Pipeline — Full Remap
# ============================================================================

def run_remap(conn, cur, logger: logging.Logger) -> dict:
    """
    Full remap: recompute all topics from scratch.
    Truncates nlp_topics and nlp_video_topics, rebuilds from all active
    embedded videos, saves new frozen model to models/bertopic/.

    Returns run statistics dict.
    """
    start_time = time.time()
    logger.info("")
    logger.info("=" * 60)
    logger.info("  MODE: FULL REMAP")
    logger.info("  All topics will be recomputed from scratch.")
    logger.info("  nlp_topics and nlp_video_topics will be truncated.")
    logger.info("=" * 60)
    logger.info("")

    # ----------------------------------------------------------------
    # STEP 1: Load all active embedded videos
    # ----------------------------------------------------------------
    logger.info("Loading embeddings for all active videos...")
    cur.execute("""
        SELECT
            ne.video_id,
            ne.embedding,
            dv.category_id,
            dc.category_name,
            COALESCE(dv.title, '') AS title
        FROM   yt.nlp_embeddings ne
        JOIN   yt.dim_videos dv      ON ne.video_id = dv.video_id
        LEFT JOIN yt.dim_categories dc ON dv.category_id = dc.category_id
        WHERE  dv.status = 'active'
        AND    ne.embed_failed_at IS NULL
        ORDER  BY ne.video_id
    """)
    rows = cur.fetchall()

    video_ids           = [r[0] for r in rows]
    embeddings          = np.array([r[1] for r in rows], dtype=np.float32)
    video_category_map  = {r[0]: r[2] for r in rows}
    category_name_map   = {r[2]: r[3] for r in rows if r[2] is not None}
    documents           = [r[4] if r[4].strip() else "unknown" for r in rows]

    logger.info(f"Loaded {len(video_ids):,} embeddings  "
                f"(shape: {embeddings.shape})")

    # ----------------------------------------------------------------
    # STEP 2: Build and fit BERTopic model
    # ----------------------------------------------------------------
    logger.info("")
    logger.info("Configuring UMAP (numba-accelerated)...")
    umap_model = UMAP(
        n_neighbors=N_NEIGHBORS,
        n_components=N_COMPONENTS,
        metric="cosine",
        random_state=RANDOM_STATE,
        low_memory=False,       # Faster on machines with enough RAM
        verbose=False
    )

    logger.info("Configuring HDBSCAN...")
    hdbscan_model = HDBSCAN(
        min_cluster_size=MIN_CLUSTER_SIZE,
        min_samples=MIN_SAMPLES,
        metric="euclidean",     # Post-UMAP space uses euclidean
        cluster_selection_method="eom",
        prediction_data=True    # Required for incremental assignment later
    )

    logger.info("Building BERTopic model...")
    vectorizer = CountVectorizer(
        stop_words="english",   # Filters out she/her/this/when etc.
        min_df=2,               # Word must appear in at least 2 documents
        ngram_range=(1, 2)      # Allows two-word phrases e.g. "war drugs"
    )
    topic_model = BERTopic(
        umap_model=umap_model,
        hdbscan_model=hdbscan_model,
        vectorizer_model=vectorizer,
        verbose=False,
        calculate_probabilities=False
    )

    logger.info("Fitting BERTopic on embeddings (this is the slow step)...")
    logger.info(f"  Min cluster size: {MIN_CLUSTER_SIZE}")
    logger.info(f"  UMAP n_neighbors: {N_NEIGHBORS}")
    logger.info(f"  UMAP n_components: {N_COMPONENTS}")
    logger.info(f"  Random state: {RANDOM_STATE}")
    logger.info("")

    fit_start = time.time()
    topics_raw, _ = topic_model.fit_transform(
        documents=documents,    # Titles used for keyword extraction (c-TF-IDF)
        embeddings=embeddings   # Embeddings drive the actual clustering
    )
    fit_duration = time.time() - fit_start
    logger.info(f"BERTopic fit complete in {fit_duration:.1f}s")

    # ----------------------------------------------------------------
    # STEP 3: Extract topic info
    # ----------------------------------------------------------------
    topic_info  = topic_model.get_topic_info()
    # Topic -1 is the outlier bucket — exclude from DB writes
    valid_topics = topic_info[topic_info["Topic"] != -1]
    n_topics    = len(valid_topics)
    n_outliers  = int((np.array(topics_raw) == -1).sum())

    logger.info(f"Topics discovered:   {n_topics:,}")
    logger.info(f"Outlier videos (-1): {n_outliers:,}  (left unassigned)")

    # ----------------------------------------------------------------
    # STEP 4: Generate labels via Ollama
    # ----------------------------------------------------------------
    logger.info("")
    logger.info("Generating topic labels via Ollama (Llama 3.2 3B)...")
    logger.info("  (Ensure Ollama is running: ollama serve)")
    logger.info("")

    topic_rows      = []    # For nlp_topics inserts
    report_topics   = []    # For the human-readable report

    # Build video→topic mapping for category assignment
    vid_to_topic = {
        vid: topic_id
        for vid, topic_id in zip(video_ids, topics_raw)
        if topic_id != -1
    }

    # Group video_ids by topic
    topic_video_groups: dict[int, list[str]] = {}
    for vid, tid in vid_to_topic.items():
        topic_video_groups.setdefault(tid, []).append(vid)

    with tqdm(total=n_topics, unit="topic",
              desc="Labelling", dynamic_ncols=True) as pbar:

        for _, row in valid_topics.iterrows():
            bertopic_id     = int(row["Topic"])
            topic_terms     = topic_model.get_topic(bertopic_id)
            if isinstance(topic_terms, list):
                keywords = [w for w, _ in topic_terms]
            else:
                logger.warning(f"Unexpected topic terms for topic {bertopic_id}: {type(topic_terms).__name__}")
                keywords = []
            if not keywords:
                keywords = ["unknown"]

            # Majority-vote category assignment
            cluster_vids    = topic_video_groups.get(bertopic_id, [])
            category_id     = assign_category(cluster_vids, video_category_map)
            category_name   = category_name_map.get(category_id, "Unknown")

            # LLM label
            label = generate_label_ollama(keywords, category_name, logger)

            topic_rows.append({
                "bertopic_id":  bertopic_id,
                "topic_label":  label,
                "keywords":     keywords[:15],
                "category_id":  category_id,
                "video_count":  len(cluster_vids),
            })

            report_topics.append({
                "topic_id":     bertopic_id,
                "topic_label":  label,
                "keywords":     keywords,
                "category_name": category_name,
                "video_count":  len(cluster_vids),
            })

            pbar.update(1)

    # ----------------------------------------------------------------
    # STEP 5: Write to database
    # ----------------------------------------------------------------
    logger.info("")
    logger.info("Writing topics to database...")

    # Truncate existing topic data (full remap replaces everything)
    cur.execute("TRUNCATE yt.nlp_video_topics CASCADE;")
    cur.execute("TRUNCATE yt.nlp_topics CASCADE;")
    conn.commit()

    # Build a bertopic_id → db topic_id map after insert
    bertopic_to_db: dict[int, int] = {}

    for t in topic_rows:
        cur.execute(
            """
            INSERT INTO yt.nlp_topics
                (topic_label, keywords, model_name, model_version, category_id)
            VALUES (%s, %s, %s, %s, %s)
            RETURNING topic_id
            """,
            (
                t["topic_label"],
                t["keywords"],
                MODEL_NAME,
                MODEL_VERSION,
                t["category_id"],
            )
        )
        db_topic_id = cur.fetchone()[0]
        bertopic_to_db[t["bertopic_id"]] = db_topic_id

    conn.commit()
    logger.info(f"  Inserted {len(topic_rows):,} topics into nlp_topics")

    # Insert video→topic assignments
    logger.info("Writing video topic assignments...")
    assignment_rows = []
    videos_assigned = 0

    for vid, bertopic_id in vid_to_topic.items():
        if bertopic_id in bertopic_to_db:
            assignment_rows.append((vid, bertopic_to_db[bertopic_id], 1.0))

    if assignment_rows:
        execute_values(
            cur,
            """
            INSERT INTO yt.nlp_video_topics (video_id, topic_id, score)
            VALUES %s
            ON CONFLICT (video_id, topic_id) DO NOTHING
            """,
            assignment_rows,
            page_size=1000
        )
        conn.commit()
        videos_assigned = len(assignment_rows)

    logger.info(f"  Assigned {videos_assigned:,} videos to topics")

    # ----------------------------------------------------------------
    # STEP 6: Save model to disk
    # ----------------------------------------------------------------
    metadata = {
        "run_mode":         "remap",
        "run_at":           datetime.now(timezone.utc).isoformat(),
        "model_name":       MODEL_NAME,
        "model_version":    MODEL_VERSION,
        "min_cluster_size": MIN_CLUSTER_SIZE,
        "n_neighbors":      N_NEIGHBORS,
        "n_components":     N_COMPONENTS,
        "random_state":     RANDOM_STATE,
        "n_topics":         n_topics,
        "n_videos":         len(video_ids),
        "n_outliers":       n_outliers,
        "fit_duration_s":   round(fit_duration, 2),
        "bertopic_to_db":   {str(k): v for k, v in bertopic_to_db.items()},
    }
    save_model(topic_model, metadata, MODEL_DIR, logger)

    # ----------------------------------------------------------------
    # STEP 7: Write topic report
    # ----------------------------------------------------------------
    # Add DB topic_ids to report topics
    for t in report_topics:
        t["topic_id"] = bertopic_to_db.get(t["topic_id"], t["topic_id"])

    write_topic_report(report_topics, REPORT_DIR, "remap", logger)

    return {
        "run_mode":         "remap",
        "videos_processed": len(video_ids),
        "videos_assigned":  videos_assigned,
        "videos_outlier":   n_outliers,
        "topics_created":   n_topics,
        "topics_total":     n_topics,
        "duration":         time.time() - start_time,
    }


