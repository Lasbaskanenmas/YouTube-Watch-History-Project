def save_model(topic_model: BERTopic,
               metadata: dict,
               model_dir: Path,
               logger: logging.Logger):
    """Save the fitted BERTopic model and run metadata to models/bertopic/."""
    model_dir.mkdir(parents=True, exist_ok=True)

    # Save BERTopic model (handles its own serialisation)
    model_path = model_dir / "bertopic_model"
    topic_model.save(str(model_path), serialization="pickle")
    logger.info(f"BERTopic model saved → {model_path}")

    # Save metadata
    meta_path = model_dir / "metadata.json"
    with open(meta_path, "w", encoding="utf-8") as f:
        json.dump(metadata, f, indent=2, default=str)
    logger.info(f"Metadata saved       → {meta_path}")


def load_model(model_dir: Path,
               logger: logging.Logger) -> BERTopic | None:
    """Load a previously saved BERTopic model. Returns None if not found."""
    model_path = model_dir / "bertopic_model"
    if not model_path.exists():
        return None
    logger.info(f"Loading saved model from {model_path}...")
    topic_model = BERTopic.load(str(model_path))
    logger.info("Saved model loaded.")
    return topic_model


