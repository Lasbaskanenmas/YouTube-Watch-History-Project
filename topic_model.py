def run_topic_model():
    """
    Orchestrate the topic modelling pipeline.
    Dispatches to run_remap() or run_incremental() based on the REMAP flag.
    """
    logger = setup_logger(LOG_DIR)

    logger.info(f"Run mode: {'FULL REMAP' if REMAP else 'INCREMENTAL'}")
    logger.info(f"Model:    {MODEL_NAME} / {OLLAMA_MODEL} (labels)")
    logger.info("")

    configure_numba(logger)

    # ----------------------------------------------------------------
    # Database connection
    # ----------------------------------------------------------------
    logger.info("Connecting to database...")
    conn = psycopg2.connect(**DB_CONFIG)
    conn.autocommit = False
    register_vector(conn)
    cur = conn.cursor()

    try:
        cur.execute("SET search_path TO yt, public;")

        # Ensure topic_log exists
        cur.execute(CREATE_TOPIC_LOG)
        conn.commit()

        # ----------------------------------------------------------------
        # Dispatch to correct run mode
        # ----------------------------------------------------------------
        if REMAP:
            stats = run_remap(conn, cur, logger)
        else:
            stats = run_incremental(conn, cur, logger)

        # ----------------------------------------------------------------
        # Log the run
        # ----------------------------------------------------------------
        cur.execute(
            """
            INSERT INTO yt.topic_log (
                run_mode, model_name, model_version,
                videos_processed, videos_assigned, videos_outlier,
                topics_created, topics_total, duration_seconds
            ) VALUES (%s, %s, %s, %s, %s, %s, %s, %s, %s)
            """,
            (
                stats["run_mode"], MODEL_NAME, MODEL_VERSION,
                stats["videos_processed"], stats["videos_assigned"],
                stats["videos_outlier"], stats["topics_created"],
                stats["topics_total"], round(stats["duration"], 2)
            )
        )
        conn.commit()

        # ----------------------------------------------------------------
        # Summary report
        # ----------------------------------------------------------------
        logger.info("")
        logger.info("=" * 60)
        logger.info(f"  TOPIC MODELLING COMPLETE  [{stats['run_mode'].upper()}]")
        logger.info("=" * 60)
        logger.info(f"  Duration:            {stats['duration']:.1f}s  "
                    f"({stats['duration']/60:.1f} min)")
        logger.info(f"  ─────────────────────────────────────")
        logger.info(f"  Videos processed:    {stats['videos_processed']:>8,}")
        logger.info(f"  Videos assigned:     {stats['videos_assigned']:>8,}")
        logger.info(f"  Videos outlier:      {stats['videos_outlier']:>8,}")
        logger.info(f"  Topics created:      {stats['topics_created']:>8,}")
        logger.info(f"  Topics total in DB:  {stats['topics_total']:>8,}")
        if stats["run_mode"] == "remap":
            logger.info(f"  ─────────────────────────────────────")
            logger.info(f"  Model saved → models/bertopic/")
            logger.info(f"  Report saved → logs/topic_reports/")
        logger.info("=" * 60)

    except Exception as e:
        conn.rollback()
        logger.error(f"\nFATAL ERROR: {e}")
        raise
    finally:
        cur.close()
        conn.close()
        logger.info("Database connection closed.")


# ============================================================================
# Main
# ============================================================================

if __name__ == "__main__":
    run_topic_model()
