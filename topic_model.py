def configure_numba(logger: logging.Logger):
    """
    Cap numba threads to leave 2 performance cores free for the OS.
    Prevents the machine from becoming unresponsive during UMAP computation.
    Thermal safety: M-series chips throttle gracefully but we reduce load anyway.
    """
    total_threads   = numba.get_num_threads()
    safe_threads    = max(1, total_threads - 2)
    numba.set_num_threads(safe_threads)
    logger.info(f"numba threads: {safe_threads} of {total_threads} (2 reserved for OS)")

# ============================================================================
# Detecting Keyword Language
# ============================================================================
def detect_keyword_language(keywords: list[str]) -> str:
    """
    Detect the dominant language of a topic's keywords.
    Returns an ISO 639-1 language code e.g. 'en', 'da', 'es', 'de'.
    Falls back to 'en' if detection fails.
    """
    try:
        sample = " ".join(keywords[:8])
        return detect(sample)
    except LangDetectException:
        return "en"


# ============================================================================
# Ollama Label Generation
# ============================================================================
# Ollama refusal detection — if the model refuses, fall back to keywords
REFUSAL_PATTERNS = [
    "i cannot", "i'm unable", "i am unable",
    "i apologize", "i'm sorry", "i am sorry",
    "not appropriate", "harmful content",
    "i can't", "i will not", "i won't"
]

def generate_label_ollama(keywords: list[str],
                           category_name: str,
                           logger: logging.Logger) -> str:
    """
    Generate a human-readable topic label using Ollama (Llama 3.2 3B).
    Falls back to a keyword-based label if Ollama is unavailable.

    Args:
        keywords:      Top keywords for this topic from BERTopic
        category_name: YouTube category (Level 1 context for the model)
        logger:        Logger instance

    Returns:
        A concise human-readable label string (3–5 words)
    """
    # 1. Build keyword string first
    keyword_str = ", ".join(keywords[:10])

    # 2. Detect language using keywords
    lang = detect_keyword_language(keywords)
    lang_note = ""
    if lang != "en":
        lang_map = {
            "da": "Danish", "es": "Spanish", "de": "German",
            "ka": "Georgian", "fr": "French", "nl": "Dutch",
            "no": "Norwegian", "sv": "Swedish"
        }
        lang_name = lang_map.get(lang, f"non-English ({lang})")
        lang_note = f"Note: keywords appear to be in {lang_name}.\n"
    
    # 3. Build prompt using both
    prompt = (
        f"You are labelling a YouTube topic cluster.\n"
        f"YouTube category: {category_name}\n"
        f"{lang_note}"
        f"Top keywords from this cluster: {keyword_str}\n\n"
        f"Rules:\n"
        f"- If keywords contain a band, artist, creator, or athlete name, "
        f"use it in the label (e.g. 'The War on Drugs' not 'War Music')\n"
        f"- If keywords are mostly non-English, label as "
        f"'[Language] Content' e.g. 'Danish Media Content'\n"
        f"- Be specific, not generic — avoid vague labels like "
        f"'Video Content' or 'Online Media'\n"
        f"- 3-5 words maximum\n"
        f"- Reply with ONLY the label, nothing else. "
        f"Do not explain, do not refuse, just write the label."
    )

    try:
        response = requests.post(
            OLLAMA_URL,
            json={
                "model":  OLLAMA_MODEL,
                "prompt": prompt,
                "stream": False,
                "options": {"temperature": 0.2}   # Low temp = consistent labels
            },
            timeout=OLLAMA_TIMEOUT
        )
        response.raise_for_status()
        label = response.json().get("response", "").strip()

        # Sanitise: remove quotes, limit length
        label = re.sub(r'^["\']|["\']$', '', label).strip()
        label = label[:100] if label else ""

        # Detect refusals — fall back to keyword label
        if label and any(label.lower().startswith(p) for p in REFUSAL_PATTERNS):
            logger.warning(f"Ollama refused to label: {keywords[:5]}")
            label = ""

        if label:
            return label

    except requests.ConnectionError:
        logger.warning("Ollama not reachable — falling back to keyword label")
    except requests.Timeout:
        logger.warning(f"Ollama timeout for keywords: {keyword_str[:50]}")
    except Exception as e:
        logger.warning(f"Ollama error: {e}")

    # Fallback: capitalise and join top 4 keywords
    return " / ".join(k.title() for k in keywords[:4])


# ============================================================================
# Automatic Category Assignment
# ============================================================================

def assign_category(video_ids: list[str],
                    video_category_map: dict[str, int]) -> int | None:
    """
    Majority-vote: assign the most common YouTube category_id among
    the videos in this topic cluster.

    Args:
        video_ids:          Video IDs in this topic cluster
        video_category_map: Mapping of video_id -> category_id

    Returns:
        The majority category_id, or None if no category data available
    """
    category_ids = [
        video_category_map[vid]
        for vid in video_ids
        if vid in video_category_map and video_category_map[vid] is not None
    ]
    if not category_ids:
        return None
    return Counter(category_ids).most_common(1)[0][0]


