def run_incremental(conn, cur, logger: logging.Logger) -> dict:
    """
    Incremental run: assign only NEW videos (not yet in nlp_video_topics)
    to the nearest existing topic using cosine similarity.

    Topic IDs are never changed — all historical queries remain valid.

    Returns run statistics dict.
    """
    start_time = time.time()
    logger.info("")
    logger.info("=" * 60)
    logger.info("  MODE: INCREMENTAL")
    logger.info("  Only new videos will be assigned to existing topics.")
    logger.info("  Topic IDs are frozen and unchanged.")
    logger.info("=" * 60)
    logger.info("")

    # ----------------------------------------------------------------
    # STEP 1: Load the frozen topic map
    # ----------------------------------------------------------------
    topic_model = load_model(MODEL_DIR, logger)
    if topic_model is None:
        logger.error(
            "No saved model found in models/bertopic/.\n"
            "  Run with REMAP = True first to build the initial topic map."
        )
        sys.exit(1)

    # Load metadata for bertopic_id → db topic_id mapping
    meta_path = MODEL_DIR / "metadata.json"
    if not meta_path.exists():
        logger.error("metadata.json not found in models/bertopic/. Cannot map topic IDs.")
        sys.exit(1)

    with open(meta_path, encoding="utf-8") as f:
        metadata = json.load(f)

    bertopic_to_db: dict[int, int] = {
        int(k): v for k, v in metadata.get("bertopic_to_db", {}).items()
    }

    # ----------------------------------------------------------------
    # STEP 2: Find new videos not yet assigned
    # ----------------------------------------------------------------
    logger.info("Finding new unassigned active videos...")
    cur.execute("""
        SELECT
            ne.video_id,
            ne.embedding
        FROM   yt.nlp_embeddings ne
        JOIN   yt.dim_videos dv ON ne.video_id = dv.video_id
        WHERE  dv.status = 'active'
        AND    ne.embed_failed_at IS NULL
        AND    ne.video_id NOT IN (
                   SELECT DISTINCT video_id FROM yt.nlp_video_topics
               )
        ORDER  BY ne.video_id
    """)
    rows = cur.fetchall()

    if not rows:
        logger.info("No new videos to assign. Database is up to date.")
        return {
            "run_mode":         "incremental",
            "videos_processed": 0,
            "videos_assigned":  0,
            "videos_outlier":   0,
            "topics_created":   0,
            "topics_total":     metadata.get("n_topics", 0),
            "duration":         time.time() - start_time,
        }

    video_ids   = [r[0] for r in rows]
    embeddings  = np.array([r[1] for r in rows], dtype=np.float32)
    logger.info(f"Found {len(video_ids):,} new videos to assign")

    # ----------------------------------------------------------------
    # STEP 3: Transform new embeddings through the frozen UMAP + assign topics
    # ----------------------------------------------------------------
    logger.info("Transforming embeddings through frozen UMAP...")
    topics_raw, _ = topic_model.transform(
        documents=["" for _ in video_ids],
        embeddings=embeddings
    )

    # ----------------------------------------------------------------
    # STEP 4: Write assignments
    # ----------------------------------------------------------------
    assignment_rows = []
    n_outliers      = 0

    for vid, bertopic_id in zip(video_ids, topics_raw):
        if bertopic_id == -1:
            n_outliers += 1
            continue    # Outliers left unassigned per design decision
        db_topic_id = bertopic_to_db.get(int(bertopic_id))
        if db_topic_id is not None:
            assignment_rows.append((vid, db_topic_id, 1.0))

    videos_assigned = 0
    if assignment_rows:
        logger.info(f"Writing {len(assignment_rows):,} assignments to database...")
        execute_values(
            cur,
            """
            INSERT INTO yt.nlp_video_topics (video_id, topic_id, score)
            VALUES %s
            ON CONFLICT (video_id, topic_id) DO NOTHING
            """,
            assignment_rows,
            page_size=1000
        )
        conn.commit()
        videos_assigned = len(assignment_rows)

    logger.info(f"  Assigned: {videos_assigned:,}")
    logger.info(f"  Outliers: {n_outliers:,}  (left unassigned)")

    # Get current total topic count from DB
    cur.execute("SELECT COUNT(*) FROM yt.nlp_topics")
    topics_total = cur.fetchone()[0]

    return {
        "run_mode":         "incremental",
        "videos_processed": len(video_ids),
        "videos_assigned":  videos_assigned,
        "videos_outlier":   n_outliers,
        "topics_created":   0,
        "topics_total":     topics_total,
        "duration":         time.time() - start_time,
    }


